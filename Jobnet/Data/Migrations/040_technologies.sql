-- 040: Per-job technology tagging. Curated vocabulary of languages, frameworks, cloud,
-- DBs, and tooling. The matcher scans each job's title/description/summary at refresh time
-- and records every match in job_technologies. Aliases let us treat "Postgres" and
-- "PostgreSQL" as the same entry; whole-word matching avoids "Java" hitting "Javascript".

CREATE TABLE technologies (
    id    INTEGER PRIMARY KEY,
    slug  TEXT NOT NULL UNIQUE,    -- stable identifier
    name  TEXT NOT NULL,           -- display name
    kind  TEXT NOT NULL            -- language|framework|cloud|cloud_service|db|queue|devops|monitor|ml|protocol|os
);

CREATE TABLE technology_aliases (
    id            INTEGER PRIMARY KEY,
    technology_id INTEGER NOT NULL REFERENCES technologies(id) ON DELETE CASCADE,
    alias         TEXT NOT NULL,
    UNIQUE(alias COLLATE NOCASE)
);

CREATE TABLE job_technologies (
    job_id        INTEGER NOT NULL REFERENCES jobs(id) ON DELETE CASCADE,
    technology_id INTEGER NOT NULL REFERENCES technologies(id) ON DELETE CASCADE,
    PRIMARY KEY (job_id, technology_id)
);

CREATE INDEX idx_job_technologies_tech ON job_technologies(technology_id);
CREATE INDEX idx_technology_aliases_tech ON technology_aliases(technology_id);

