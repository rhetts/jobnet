-- 021: Directory/portfolio URLs that the harvester crawls — formerly hardcoded in
-- ServiceRegistration.cs. Now data-driven so the user can edit them in Settings.
CREATE TABLE discovery_seeds (
    id           INTEGER PRIMARY KEY,
    name         TEXT NOT NULL,
    url          TEXT NOT NULL UNIQUE,
    description  TEXT,
    is_enabled   INTEGER NOT NULL DEFAULT 1,
    sort_order   INTEGER NOT NULL DEFAULT 0,
    date_added   TEXT NOT NULL DEFAULT (datetime('now'))
);

CREATE INDEX idx_discovery_seeds_enabled ON discovery_seeds(is_enabled, sort_order);

